//dd/MM/YYYY
function getFecha(fecha) {
	
	if(fecha==null){
		d=new Date();
	}else{
		d=new Date(fecha);
	}
	
	var day = d.getDate();
	if (day < 10) {
		day = "0" + day;
	}
	var month = d.getMonth() + 1;
	var year = d.getFullYear();

	if (month < 10) {
		month = "0" + month;
	}
	return day + "/" + month + "/" + year
}

